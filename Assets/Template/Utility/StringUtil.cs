namespace Template.Utility
{
    public class StringUtil
    {
        public static string PathBuilder(string a, string b) { return (a + b); }
        public static string PathBuilder(string a, string b, string c) { return (a + b + c); }
        public static string PathBuilder(string a, string b, string c, string d) { return (a + b + c + d); }
    }
}
